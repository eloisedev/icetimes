  function generateCalendar() {
    const calendarBody = document.getElementById("calendar-body");
    const firstDay = new Date("2025-01-01T00:00:00").getDay(); // Ensure midnight UTC
    const daysInMonth = 31; // Days in January
    let date = 1;
  
    // Clear any existing calendar content
    calendarBody.innerHTML = "";
  
    for (let i = 0; i < 6; i++) {
      const row = document.createElement("tr");
  
      for (let j = 0; j < 7; j++) {
        const cell = document.createElement("td");
  
        if (i === 0 && j < firstDay) {
          cell.textContent = ""; // Empty cells before the first day
        } else if (date > daysInMonth) {
          break; // Stop if we've filled the calendar
        } else {
          cell.textContent = date;
          cell.setAttribute("data-date", `2025-01-${String(date).padStart(2, "0")}`);
          cell.addEventListener("click", showEvents);
          date++;
        }
  
        row.appendChild(cell);
      }
  
      calendarBody.appendChild(row);
    }
  }
  
  function showEvents(event) {
    const selectedDate = event.target.getAttribute("data-date");
    const eventList = document.getElementById("event-list");
    const container = document.querySelector(".container2");
  
    if (!eventList) {
      console.error("Event list element not found.");
      return;
    }
  
    eventList.innerHTML = ""; // Clear previous events
  
    if (container) {
      container.scrollTop = 0; // Scroll to the top
    }
  
    if (eventsData[selectedDate]) {
      eventList.style.display = "flex";
      eventList.style.flexWrap = "wrap";
  
      eventsData[selectedDate].forEach((event) => {
        const item = document.createElement("div");
        item.classList.add("item1");
  
        // Format the date properly
        const dateParagraph = document.createElement("p");
        const formattedDate = new Date(`${selectedDate}T00:00:00`);
        dateParagraph.textContent = formattedDate.toLocaleString("en-US", {
          month: "short",
          day: "numeric",
        });
        item.appendChild(dateParagraph);
  
        const typeHeader = document.createElement("h4");
        typeHeader.textContent = event.type;
        item.appendChild(typeHeader);
  
        const timeHeader = document.createElement("h1");
        timeHeader.textContent = event.time;
        item.appendChild(timeHeader);
  
        const locationHeader = document.createElement("h4");
        locationHeader.textContent = event.location;
        item.appendChild(locationHeader);
  
        const bottomTagDiv = document.createElement("div");
        bottomTagDiv.classList.add("bottomtag");
  
        // Create the link and button
        const signUpLink = document.createElement("a");
        signUpLink.href = event.link || "#";
        signUpLink.target = "_blank"; // Open link in new tab
        signUpLink.rel = "noopener noreferrer";
  
        const signUpButton = document.createElement("button");
        signUpButton.textContent = "Sign Up";
        signUpLink.appendChild(signUpButton);
  
        bottomTagDiv.appendChild(signUpLink);
        item.appendChild(bottomTagDiv);
  
        eventList.appendChild(item);
      });
    } else {
      eventList.style.display = "block"; // Ensure display is visible
      eventList.innerHTML = "<li>No events scheduled</li>";
    }
  }
  
  // Initialize calendar on page load
  window.onload = function () {
    generateCalendar();
  };
  
